package Proje1;

public class GiftWriter {
	private String bike;
	private String pencil;
	private String tabletPc;

	String getKalem() {
		return pencil;
	}

	void setKalem(String kalem) {
		this.pencil = kalem;
	}

	String getBisiklet() {
		return bike;
	}

	void setBisiklet(String bisiklet) {
		this.bike = bisiklet;
	}

	String getTabletPc() {
		return tabletPc;
	}

	void setTabletPc(String tabletPc) {
		this.tabletPc = tabletPc;
	}
}
